using System;
using System.Collections.Generic;
using System.Text;
using System.Windows.Forms;

namespace XenAdmin.Controls
{
    // Windows forms don't really become transparent even when you specify their background
    // colour as transparent. This hack is found on many pages to turn a panel into
    // a true transparent panel (e.g. http://www.bobpowell.net/transcontrols.htm)

    class TransparentPanel : Panel
    {
        protected override CreateParams CreateParams
        {
            get
            {
                CreateParams cp = base.CreateParams;
                cp.ExStyle |= 0x00000020;
                return cp;
            }
        }

        protected override void OnPaintBackground(PaintEventArgs pevent)
        {
        }
    }
}
